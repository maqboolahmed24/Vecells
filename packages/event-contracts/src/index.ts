export interface FoundationEventEnvelope<TPayload> {
  eventType: string;
  emittedAt: string;
  payload: TPayload;
}

export function makeFoundationEvent<TPayload>(
  eventType: string,
  payload: TPayload,
): FoundationEventEnvelope<TPayload> {
  return {
    eventType,
    emittedAt: new Date().toISOString(),
    payload,
  };
}

